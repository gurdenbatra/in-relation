/**
 * Sync per-walk folders (images/Walk N - …/) → public/images/walks/N/*.webp
 * Writes src/data/walkImageAssets.js. Copies .MOV as-is.
 * Cleans legacy flat files in public/images and images/ (root only).
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { convertRasterToWebp } from './convert-images.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const REPO = path.join(__dirname, '..')
const IMAGES_DIR = path.join(REPO, 'images')
const PUBLIC_IMAGES = path.join(REPO, 'public', 'images')
const ASSETS_OUT = path.join(REPO, 'src', 'data', 'walkImageAssets.js')

const RASTER_EXT = new Set(['.heic', '.jpg', '.jpeg', '.png', '.webp'])
const VIDEO_EXT = new Set(['.mov', '.MOV', '.mp4', '.MP4'])

const WALK_DIR_RE = /^Walk (\d+)/

function safeSegment(name) {
  return path
    .basename(name, path.extname(name))
    .replace(/ /g, '-')
    .replace(/[^a-zA-Z0-9._-]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .normalize('NFC')
}

function listWalkDirs() {
  if (!fs.existsSync(IMAGES_DIR)) return []
  return fs
    .readdirSync(IMAGES_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .filter((name) => WALK_DIR_RE.test(name))
}

function listRasterAndVideo(walkDirPath) {
  const out = []
  for (const name of fs.readdirSync(walkDirPath)) {
    const full = path.join(walkDirPath, name)
    if (fs.statSync(full).isDirectory()) continue
    const ext = path.extname(name)
    if (RASTER_EXT.has(ext.toLowerCase()) || VIDEO_EXT.has(ext)) out.push(name)
  }
  return out
}

async function syncWalkFolder(walkId, folderName, walksRootDir) {
  const srcDir = path.join(IMAGES_DIR, folderName)
  const destDir = path.join(walksRootDir, String(walkId))
  fs.mkdirSync(destDir, { recursive: true })

  const urls = []
  const files = listRasterAndVideo(srcDir)

  for (const filename of files) {
    const srcPath = path.join(srcDir, filename)
    const ext = path.extname(filename).toLowerCase()

    if (VIDEO_EXT.has(path.extname(filename))) {
      const destName = path.basename(filename).replace(/ /g, '-').normalize('NFC')
      const destPath = path.join(destDir, destName)
      fs.copyFileSync(srcPath, destPath)
      console.log(`  copy  ${folderName}/${filename} → walks/${walkId}/${destName}`)
      continue
    }

    if (!RASTER_EXT.has(ext)) continue

    const base = safeSegment(filename)
    const outBase = `${walkId}-${base}.webp`
    const outPath = path.join(destDir, outBase)

    console.log(`  webp  ${folderName}/${filename}`)
    await convertRasterToWebp(srcPath, outPath, ext)
    urls.push(`/images/walks/${walkId}/${outBase}`)
  }

  return urls.sort()
}

async function syncFoundersPhoto() {
  const candidates = ['IMG_2679.HEIC', 'IMG_2679.heic', 'IMG_2679.jpg', 'IMG_2679.jpeg', 'IMG_2679.png']
  for (const c of candidates) {
    const src = path.join(IMAGES_DIR, c)
    if (!fs.existsSync(src)) continue
    const dest = path.join(PUBLIC_IMAGES, 'IMG_2679.webp')
    const ext = path.extname(c).toLowerCase()
    console.log(`  webp  ${c} (founders)`)
    await convertRasterToWebp(src, dest, ext)
    return
  }
}

function writeAssetsFile(assetsById) {
  const lines = [
    '/** Auto-generated by scripts/sync-walk-images.js — run npm run sync-walk-images */',
    'export const walkImageAssets = {',
  ]
  const ids = Object.keys(assetsById)
    .map(Number)
    .sort((a, b) => a - b)
  for (const id of ids) {
    const urls = assetsById[id]
    const json = JSON.stringify(urls, null, 2)
      .split('\n')
      .map((line, i) => (i === 0 ? line : `  ${line}`))
      .join('\n')
    lines.push(`  ${id}: ${json},`)
  }
  lines.push('}')
  lines.push('')
  fs.writeFileSync(ASSETS_OUT, lines.join('\n'), 'utf8')
  console.log(`Wrote ${ASSETS_OUT}`)
}

const SOURCE_DELETE_EXT = new Set(['.heic', '.jpg', '.jpeg', '.png', '.webp', '.mov', '.mp4'])

function deleteFlatFilesInImagesRoot() {
  if (!fs.existsSync(IMAGES_DIR)) return
  for (const name of fs.readdirSync(IMAGES_DIR)) {
    const full = path.join(IMAGES_DIR, name)
    if (fs.statSync(full).isDirectory()) continue
    const ext = path.extname(name).toLowerCase()
    if (!SOURCE_DELETE_EXT.has(ext)) continue
    fs.unlinkSync(full)
    console.log(`  removed images/${name}`)
  }
}

function cleanLegacyPublicImages() {
  if (!fs.existsSync(PUBLIC_IMAGES)) return
  const keep = new Set(['IMG_2679.webp'])
  for (const name of fs.readdirSync(PUBLIC_IMAGES)) {
    const full = path.join(PUBLIC_IMAGES, name)
    if (fs.statSync(full).isDirectory()) {
      if (name === 'walks' || name.startsWith('.walks-staging-')) continue
      fs.rmSync(full, { recursive: true, force: true })
      console.log(`  removed public/images/${name}/`)
      continue
    }
    if (keep.has(name)) continue
    const ext = path.extname(name).toLowerCase()
    if (['.webp', '.jpg', '.jpeg', '.png', '.mov', '.mp4'].includes(ext)) {
      fs.unlinkSync(full)
      console.log(`  removed public/images/${name}`)
    }
  }
}

/**
 * Remove staging dirs left after a crashed sync. Skip dirs owned by a live process
 * (another concurrent `sync-walk-images`); name embeds `.walks-staging-<pid>-<time>/`.
 */
function removeStaleWalkStagingDirs() {
  if (!fs.existsSync(PUBLIC_IMAGES)) return
  for (const name of fs.readdirSync(PUBLIC_IMAGES)) {
    if (!name.startsWith('.walks-staging-')) continue
    const m = name.match(/^\.walks-staging-(\d+)-/)
    const ownerPid = m ? Number(m[1]) : NaN
    if (!Number.isFinite(ownerPid)) continue
    try {
      process.kill(ownerPid, 0)
      continue
    } catch (e) {
      if (e.code !== 'ESRCH') continue
    }
    const full = path.join(PUBLIC_IMAGES, name)
    fs.rmSync(full, { recursive: true, force: true })
    console.log(`  removed stale ${name}/`)
  }
}

async function run() {
  fs.mkdirSync(PUBLIC_IMAGES, { recursive: true })
  removeStaleWalkStagingDirs()

  cleanLegacyPublicImages()

  const walksPublic = path.join(PUBLIC_IMAGES, 'walks')
  const walksStaging = path.join(
    PUBLIC_IMAGES,
    `.walks-staging-${process.pid}-${Date.now()}`,
  )
  fs.mkdirSync(walksStaging, { recursive: true })

  const assetsById = {}
  const walkDirs = listWalkDirs().sort((a, b) => {
    const na = Number(a.match(WALK_DIR_RE)?.[1] ?? 0)
    const nb = Number(b.match(WALK_DIR_RE)?.[1] ?? 0)
    return na - nb
  })
  for (const folderName of walkDirs) {
    const m = folderName.match(WALK_DIR_RE)
    const walkId = Number(m[1])
    assetsById[walkId] = await syncWalkFolder(walkId, folderName, walksStaging)
  }

  if (fs.existsSync(walksPublic)) {
    fs.rmSync(walksPublic, { recursive: true, force: true })
  }
  fs.renameSync(walksStaging, walksPublic)

  await syncFoundersPhoto()
  writeAssetsFile(assetsById)
  deleteFlatFilesInImagesRoot()

  console.log('Walk image sync complete.')
}

run().catch((err) => {
  console.error(err)
  process.exit(1)
})
